/**
 * The EmploymentCategory file is responsible for representing the various employment categories a person can have.
 */
package org.soap.api.models;

/**
 * The EmploymentCategory class is responsible for representing the various
 * employment categories a person can have.
 *
 * @author Zac Healy
 *
 * @version 1.0
 * @since 2023-10-11
 */
public enum EmploymentCategory {
	
	/**
	 * Represents the Healthcare employment category.
	 */
	HEALTHCARE,

	/**
	 * Represents the Engineering & Technology employment category.
	 */
	ENGINEERING_TECHNOLOGY,

	/**
	 * Represents the Science & Research employment category.
	 */
	SCIENCE_RESEARCH,

	/**
	 * Represents the Management & Consulting employment category.
	 */
	MANAGEMENT_CONSULTING,

	/**
	 * Represents the Media & Journalism employment category.
	 */
	MEDIA_JOURNALISM,

	/**
	 * Represents the Legal employment category.
	 */
	LEGAL,

	/**
	 * Represents the Psychology & Counseling employment category.
	 */
	PSYCHOLOGY_COUNSELING,

	/**
	 * Represents the Finance employment category.
	 */
	FINANCE,

	/**
	 * Represents the History & Archaeology employment category.
	 */
	HISTORY_ARCHEOLOGY,

	/**
	 * Represents the Design & Art employment category.
	 */
	DESIGN_ART,

	/**
	 * Represents the Education employment category.
	 */
	EDUCATION,

	/**
	 * Represents an Other or unspecified employment category.
	 */
	OTHER
}
